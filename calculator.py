def result(x, y, sign):
    """
    The function performs the simplest mathematical calculations
    """

    if sign == '+':
        result = x + y
    elif sign == '-':
        result = x - y
    elif sign == '/':
        result = x / y
    elif sign == '*':
        result = x * y
    elif sign == '**':
        result = x ** y
    return result

